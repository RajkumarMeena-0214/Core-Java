package in.co.raysBasics;

public class SwitchCase {
	 public static void main (String [] args) {
		 int num = 30; 
		 switch (num) {
			 
		 case 10:
			 System.out.println("Case 1 Satisfy");
			 break;
		 case 20:
			 System.out.println("Case 2 Satisfy");
			 break;
		 case 30:
			 System.out.println("Case 3 Satisfy");
			 break;
		 default:
			 System.out.println("This block is Default");
			 break;
		 }
	 }

}
