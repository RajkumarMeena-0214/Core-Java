package in.co.raysBasics;

public class CalculatorIfelse {
	public static void main (String [] args) {
	    String s = "+";
	    
	    int a = 20;
		int b = 30;
		
        if (s=="+"){
			System.out.println(a+b);
		}else if (s=="-") {
			System.out.println(a-b);
		}else if (s=="*"){
			System.out.println(a*b);
		}else if (s=="/") {
			System.out.println(a/b);
			
		}
	}
}
	
	
